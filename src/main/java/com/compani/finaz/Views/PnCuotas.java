    private void calcularImporte() {
        CuotaTableModel tmCuota = (CuotaTableModel) jTable1.getModel();
        tfTotalImporte.setText(String.valueOf(tmCuota.getMonto()));
        tfNoCuotas.setText(String.valueOf(tmCuota.getNoCuota()));
    }

