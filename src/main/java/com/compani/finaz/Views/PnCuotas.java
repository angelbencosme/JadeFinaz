    private void setSolicitud(SolicitudAutorizacion solicitud) {

        tfNumeroDocumento.setText(solicitud.getSolicitud().getNoSolicitud());
        tfnombreCliente.setText(solicitud.getSolicitud().getCliente().getNombre() + " " + solicitud.getSolicitud().getCliente().getApellido());
        lbcodigoCliente.setText(String.valueOf(solicitud.getSolicitud().getCliente().getId()));
        CuotasController cCuotas = new CuotasController();
        CuotaTableModel tmCuotas = new CuotaTableModel();
        List list = cCuotas.getlCuotas(solicitud.getId());
        tmCuotas.setlCuota(list);
        jTable1.setModel(tmCuotas);
        TableColumn tableColumn = jTable1.getColumnModel().getColumn(1);
        JComboBox cbtipoAsistencia;
        List list5 = new ArrayList<>();
        list5.add("PAGO");

        cbtipoAsistencia = new JComboBox(list5.toArray());
        tableColumn.setCellEditor(new DefaultCellEditor(cbtipoAsistencia));
    }

