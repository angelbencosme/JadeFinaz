    @Override
    public void save() {
        CuotaTableModel tcuotas = (CuotaTableModel) jTable1.getModel();
        List list = tcuotas.getlCuota();
        
        for (Iterator it = list.iterator(); it.hasNext();) {
            Cuota cuotas = (Cuota) it.next(); 
            cCuota.save(cuotas);
        }
        
       
    } 

