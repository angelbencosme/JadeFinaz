    private boolean filter() {
        if (cbTipoIndentificacion.getSelectedItem().toString().equals("Pasaporte") && tfIdentificacion.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes especificar correctamente la Pasaporte");
            tfIdentificacion.requestFocus();
            return false;
        }
        if (cbTipoIndentificacion.getSelectedItem().toString().equals("Cedula") && tfIdentificacion.getText().trim().length() != 13) {
            JOptionPane.showMessageDialog(this, "Debes especificar correctamente la Cedula");
            tfIdentificacion.requestFocus();
            return false;
        }
        //Datos PErsonales        
        if (tfNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar el Nombre");
            tfNombre.requestFocus();
            return false;
        }

        if (tfApellido.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar el Apellido");
            tfApellido.requestFocus();
            return false;
        }

        if (dateFechaNacimiento.getDate().toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar la Fcha de nacimiento");
            dateFechaNacimiento.requestFocus();
            return false;
        }

        if (cbEstadoCivil.getSelectedIndex() <= -1) {
            JOptionPane.showMessageDialog(this, "Debes especificar el estado civil");
            cbEstadoCivil.requestFocus();
            return false;
        }
        if (cbSexo.getSelectedIndex() <= -1) {
            JOptionPane.showMessageDialog(this, "Debes especificar el sexo");
            cbSexo.requestFocus();
            return false;
        }
        if (tfTelefono.getText().length() != 13) {
            JOptionPane.showMessageDialog(this, "Debes especificar el teléfono");
            tfTelefono.requestFocus();
            return false;
        }
        if (tfCelular.getText().length() != 13) {
            JOptionPane.showMessageDialog(this, "Debes especificar el celular");
            tfCelular.requestFocus();
            return false;
        }
        if (tfEmail.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar el E-mail");
            tfEmail.requestFocus();
            return false;
        }
        if (jsLimite.getValue().toString().length() <= 0) {
            JOptionPane.showMessageDialog(this, "Debes especificar el límite");
            jsLimite.requestFocus();
            return false;
        }
//        if (dateFechaRegistro.getDate().toString().length() <= 0) {
//            JOptionPane.showMessageDialog(this, "Debes especificar la fecha de registro");
//            dateFechaRegistro.requestFocus();
//            return false;
//        }
        if (tfOcupacion.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar la ocupación");
            tfOcupacion.requestFocus();
            return false;
        }
        if (tfCuidad.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar la cuidad");
            tfCuidad.requestFocus();
            return false;
        }
        if (tfDireccion.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar la dirección");
            tfTelefono.requestFocus();
            return false;
        }
        //Datos Laborales
        if (tfEmpresa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar la empresa");
            tfEmpresa.requestFocus();
            return false;
        }
        if (tfCargo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar el cargo");
            tfCargo.requestFocus();
            return false;
        }
        if (tfIngreso.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar el ingreso");
            tfIngreso.requestFocus();
            return false;
        }
        if (tfDepartamento.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar el departamento en que trabaja");
            tfDepartamento.requestFocus();
            return false;
        }
        if (dateFechaIngreso.getDate().toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar la fecha que ingreso al trabajo");
            dateFechaIngreso.requestFocus();
            return false;
        }
        if (tfJefe.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar el jefe de trabajo");
            tfDepartamento.requestFocus();
            return false;
        }
        if (tfCuidad.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes especificar la cuidad donde trabaja");
            tfCuidad.requestFocus();
            return false;
        }
        if (tfTelefonoTrabajo.getText().length() != 13) {
            JOptionPane.showMessageDialog(this, "Debes especificar el telefono del trabajo");
            tfTelefonoTrabajo.requestFocus();
            return false;
        }

        return true;

    }

