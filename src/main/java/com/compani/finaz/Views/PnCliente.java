    private void FormatterTF() {
        String cb = (String) cbTipoIndentificacion.getSelectedItem();
        if ("Cedula".equals(cb)) {
            try {
                tfIdentificacion.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("###-#######-#")));
            } catch (ParseException ex) {
                Logger.getLogger(PnPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if ("Pasaporte".equals(cb)) {
            tfIdentificacion.setFormatterFactory(null);
            tfIdentificacion.setText("");
        }
    }

